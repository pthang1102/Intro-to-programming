# How to print several complex strings

# Print string that has quotes in it, using 'escape character' \
print('\'I want to study to code!\', he said.')

# Print string that has quotes in it, using another quotes
print('"I want to study to code!", he said.')

# Break line
print("New\nline")

# Break lines even more easily using 'Triple quotes'
print("""
This is an easier way
To print strings
Without using too many
New-line characters
""")